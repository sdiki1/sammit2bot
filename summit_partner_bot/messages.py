MESSAGE_DEFAULTS: dict[str, str] = {key: default for key, _, default in MESSAGE_REGISTRY}


def resolve_message(settings_map: dict[str, str], key: str, **kwargs: object) -> str:
    """Возвращает текст по ключу с подменой из settings_map и форматированием."""
    text = (settings_map.get(key) or "").strip() or MESSAGE_DEFAULTS.get(key, "")
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, IndexError, ValueError):
            return text
    return text
